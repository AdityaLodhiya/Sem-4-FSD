expr=require("express")
app=expr()
cp=require("cookie-parser")
app.use(cp())
app.get("/",(req,res)=>{
    res.cookie("fname","abc")
    res.cookie("lname","pqr")
    res.cookie("email","abc@gmail.com",{"maxAge":"10000"})
    res.cookie("contect","123456789")
    res.clearCookie("contect")
    res.send(req.cookies)
})
app.listen(5678)